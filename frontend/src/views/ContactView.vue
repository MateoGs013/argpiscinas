<script setup>
import { ref, reactive } from 'vue'
import { useSeo } from '@/composables/useSeo'
import api from '@/services/api'

useSeo({
  title: 'Contacto | Solicita tu Presupuesto',
  description: 'Contacta con ARG Piscinas para solicitar presupuesto, consultas o información sobre nuestros servicios de instalación y rehabilitación de piscinas.',
  keywords: 'contacto piscinas, presupuesto piscinas, consulta piscinas, teléfono ARG Piscinas'
})

const form = reactive({
  name: '',
  email: '',
  phone: '',
  service: '',
  subject: '',
  message: '',
  privacy: false
})

const errors = reactive({})
const submitting = ref(false)
const submitSuccess = ref(false)

const validateForm = () => {
  const newErrors = {}
  
  if (!form.name.trim()) {
    newErrors.name = 'El nombre es obligatorio'
  }
  
  if (!form.email.trim()) {
    newErrors.email = 'El email es obligatorio'
  } else if (!/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(form.email)) {
    newErrors.email = 'El email no es válido'
  }
  
  if (!form.subject.trim()) {
    newErrors.subject = 'El asunto es obligatorio'
  }
  
  if (!form.message.trim()) {
    newErrors.message = 'El mensaje es obligatorio'
  }
  
  Object.assign(errors, newErrors)
  return Object.keys(newErrors).length === 0
}

const submitForm = async () => {
  if (!validateForm()) return
  
  submitting.value = true
  
  try {
    await api.post('/contacts', {
      name: form.name,
      email: form.email,
      phone: form.phone,
      subject: form.subject,
      message: form.message,
      service: form.service
    })
    
    submitSuccess.value = true
    
    // Reset form
    form.name = ''
    form.email = ''
    form.phone = ''
    form.service = ''
    form.subject = ''
    form.message = ''
    form.privacy = false
    
    // Hide success message after 5 seconds
    setTimeout(() => {
      submitSuccess.value = false
    }, 5000)
  } catch (error) {
    console.error('Error submitting form:', error)
    alert('Ha ocurrido un error. Por favor, inténtalo de nuevo.')
  } finally {
    submitting.value = false
  }
}
</script>
